// Funci√≥n para obtener el token de sesi√≥n del frontend
async function getSessionToken() {
  console.log('üîê Obteniendo token de sesi√≥n del frontend...');
  
  try {
    // Intentar acceder a la sesi√≥n a trav√©s del contexto de React
    // Buscar en el DOM elementos que puedan contener informaci√≥n de sesi√≥n
    const userElements = document.querySelectorAll('[data-user-id], [data-user], [data-session]');
    console.log('üë§ Elementos de usuario encontrados:', userElements.length);
    
    // Buscar en localStorage
    const supabaseSession = localStorage.getItem('sb-tenerifeparadisetoursexcursions-auth-token');
    if (supabaseSession) {
      console.log('üíæ Sesi√≥n encontrada en localStorage');
      try {
        const sessionData = JSON.parse(supabaseSession);
        if (sessionData.access_token) {
          console.log('‚úÖ Token encontrado en localStorage');
          return sessionData.access_token;
        }
      } catch (e) {
        console.log('‚ùå Error al parsear sesi√≥n de localStorage');
      }
    }
    
    // Buscar en sessionStorage
    const sessionStorageToken = sessionStorage.getItem('supabase.auth.token');
    if (sessionStorageToken) {
      console.log('üíæ Token encontrado en sessionStorage');
      try {
        const tokenData = JSON.parse(sessionStorageToken);
        if (tokenData.currentSession?.access_token) {
          console.log('‚úÖ Token encontrado en sessionStorage');
          return tokenData.currentSession.access_token;
        }
      } catch (e) {
        console.log('‚ùå Error al parsear token de sessionStorage');
      }
    }
    
    // Buscar en cookies
    const cookies = document.cookie.split(';');
    for (const cookie of cookies) {
      if (cookie.includes('supabase') || cookie.includes('auth') || cookie.includes('session')) {
        console.log('üç™ Cookie relacionada con sesi√≥n encontrada:', cookie.trim());
      }
    }
    
    console.log('‚ùå No se pudo obtener token del frontend');
    return null;
    
  } catch (error) {
    console.error('‚ùå Error al obtener token:', error);
    return null;
  }
}

// Funci√≥n para verificar el estado actual
async function checkCurrentState() {
  console.log('üîç Verificando estado actual...');
  
  // Verificar si estamos en la p√°gina de booking
  const isBookingPage = window.location.pathname.includes('/booking/');
  console.log('üìç P√°gina actual:', window.location.pathname, isBookingPage ? '(‚úÖ P√°gina de booking)' : '(‚ùå No es p√°gina de booking)');
  
  // Verificar si hay formulario
  const form = document.querySelector('form');
  console.log('üìù Formulario encontrado:', !!form);
  
  // Verificar si hay bot√≥n de env√≠o
  const submitButton = document.querySelector('button[type="submit"]');
  console.log('üîò Bot√≥n de env√≠o encontrado:', !!submitButton);
  
  // Obtener token de sesi√≥n
  const token = await getSessionToken();
  console.log('üîê Token obtenido:', !!token);
  
  // Verificar sesi√≥n usando fetch con token
  if (token) {
    try {
      console.log('üîê Verificando sesi√≥n con token...');
      const response = await fetch('/api/auth/session', {
        method: 'GET',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${token}`,
        }
      });
      
      if (response.ok) {
        const sessionData = await response.json();
        console.log('‚úÖ Sesi√≥n verificada con token:', {
          hasSession: !!sessionData.session,
          hasUser: !!sessionData.session?.user,
          userId: sessionData.session?.user?.id,
          method: sessionData.method
        });
      } else {
        console.log('‚ö†Ô∏è Error al verificar sesi√≥n con token');
      }
    } catch (error) {
      console.log('‚ö†Ô∏è Error al verificar sesi√≥n con token:', error.message);
    }
  }
  
  return { isBookingPage, hasForm: !!form, hasSubmitButton: !!submitButton, hasToken: !!token };
}

